public class Nasabah {
    String nama;
    String alamat;
    double saldo;
    String username;
    String password;

    public Nasabah(String nama, String alamat, String username, String password){
        this.nama = nama;
        this.alamat = alamat;
        this.saldo = 0.0;
        this.username = username;
        this.password = password;
    }
}
